create or replace function private.audit_operational_change()
returns trigger language plpgsql security definer set search_path='' as $$
declare payload jsonb; row_id text; org_id uuid;
begin
  payload := case when tg_op='DELETE' then to_jsonb(old) else to_jsonb(new) end;
  row_id := coalesce(payload->>'id',payload->>'user_id',payload->>'name','unknown');
  org_id := coalesce(nullif(payload->>'organization_id','')::uuid,
                     case when tg_table_name='organizations' then nullif(payload->>'id','')::uuid else null end);
  insert into public.audit_log(actor_id,organization_id,entity_type,entity_id,action,details)
  values ((select auth.uid()),org_id,tg_table_name,row_id,lower(tg_op),
    jsonb_build_object('before',case when tg_op in ('UPDATE','DELETE') then to_jsonb(old) else null end,
                       'after',case when tg_op in ('INSERT','UPDATE') then to_jsonb(new) else null end));
  return coalesce(new,old);
end $$;
revoke execute on function private.audit_operational_change() from public,anon,authenticated;

do $$
declare table_name text;
begin
  foreach table_name in array array['claims','projects','proposals','activities','territorial_referents','headquarters','memberships','teams','organizations']
  loop
    execute format('drop trigger if exists audit_operational on public.%I',table_name);
    execute format('create trigger audit_operational after insert or update or delete on public.%I for each row execute function private.audit_operational_change()',table_name);
  end loop;
end $$;

