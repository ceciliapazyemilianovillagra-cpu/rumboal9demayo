-- Ampliación operativa: trazabilidad, archivos, padrón, licencias y métricas.
alter table public.organizations
  add column if not exists logo_url text,
  add column if not exists plan_name text not null default 'base',
  add column if not exists license_status text not null default 'active'
    check (license_status in ('trial','active','suspended','expired')),
  add column if not exists license_expires_at timestamptz;

alter table public.claims
  add column if not exists latitude numeric(10,7),
  add column if not exists longitude numeric(10,7),
  add column if not exists resolved_at timestamptz;

alter table public.activities
  add column if not exists reminder_minutes integer not null default 60
    check (reminder_minutes between 0 and 10080);

alter table public.audit_log
  add column if not exists organization_id uuid references public.organizations(id) on delete cascade;

create index if not exists audit_log_org_created_idx
  on public.audit_log(organization_id, created_at desc);

create table if not exists public.entity_attachments (
  id bigint generated by default as identity primary key,
  organization_id uuid not null references public.organizations(id) on delete cascade,
  entity_type text not null check (entity_type in ('claim','project','budget','activity','proposal')),
  entity_id bigint not null,
  file_name text not null,
  storage_path text not null,
  content_type text,
  size_bytes bigint check (size_bytes is null or size_bytes between 0 and 10485760),
  uploaded_by uuid not null references public.profiles(id),
  created_at timestamptz not null default now()
);

create table if not exists public.voter_imports (
  id uuid primary key default gen_random_uuid(),
  organization_id uuid not null references public.organizations(id) on delete cascade,
  file_name text not null,
  file_size bigint,
  source_format text not null check (source_format in ('csv','xlsx','other')),
  status text not null default 'analisis'
    check (status in ('analisis','mapeo','listo','procesando','completado','con_errores','cancelado')),
  detected_columns jsonb not null default '[]'::jsonb,
  column_mapping jsonb not null default '{}'::jsonb,
  total_rows bigint not null default 0,
  processed_rows bigint not null default 0,
  error_rows bigint not null default 0,
  notes text,
  created_by uuid not null references public.profiles(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists voter_imports_org_created_idx
  on public.voter_imports(organization_id, created_at desc);
create index if not exists voters_org_polling_idx
  on public.voters(organization_id, polling_place);

create table if not exists public.operational_targets (
  id bigint generated by default as identity primary key,
  organization_id uuid not null references public.organizations(id) on delete cascade,
  metric text not null check (metric in ('reclamos_resueltos','actividades','barrios_cubiertos','referentes_activos','proyectos_completados')),
  period_start date not null,
  period_end date not null,
  target_value numeric(14,2) not null check (target_value >= 0),
  created_by uuid not null references public.profiles(id),
  created_at timestamptz not null default now(),
  unique(organization_id, metric, period_start, period_end),
  check(period_end >= period_start)
);

alter table public.entity_attachments enable row level security;
alter table public.voter_imports enable row level security;
alter table public.operational_targets enable row level security;

